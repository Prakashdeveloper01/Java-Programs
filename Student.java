class Student 
{
private String name;
private int Regno;
private int number;
private String emailid;

public Student(String name,int Regno,int number,String emailid)
{
this.name=name;
this.Regno=Regno;
this.number=number;
this.emailid=emailid;
}

public void setName()
{
this.name=name;
}
public String getName()
{
return name;
}
public void setRegno()
{
this.Regno=Regno;
}
public int getRegno()
{
return Regno;
}
public void setNumber()
{
this.number=number;
}
public int getNumber()
{
return number;
}
public void setEmailid()
{
this.emailid=emailid;
}
public String getEmailid()
{
return emailid;
}
}